def get_pro_by_id(pro_id: str):
    """Get pro player info by ID"""
    return PRO_PLAYERS.get(pro_id.lower())


def get_pros_by_league(league: str):
    """Get all pros from a specific league"""
    return {k: v for k, v in PRO_PLAYERS.items() if v['league'] == league}


def get_pros_by_team(team: str):
    """Get all pros from a specific team"""
    return {k: v for k, v in PRO_PLAYERS.items() if v['team'] == team}


def get_all_teams():
    """Get list of all unique teams organized by league"""
    teams_by_league = {}
    for player in PRO_PLAYERS.values():
        league = player['league']
        team = player['team']
        if league not in teams_by_league:
            teams_by_league[league] = set()
        teams_by_league[league].add(team)

    # Convert sets to sorted lists
    return {league: sorted(list(teams)) for league, teams in teams_by_league.items()}
